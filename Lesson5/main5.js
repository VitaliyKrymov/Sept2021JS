function moveZeroToEndOfArr(arr) {
    let address = 0;
    let length = arr.length;
    for (let i = 0; i < length; i++) {
        if (arr[i] === 0) {
            address = i;
            for (let j = address; j < length - 1; j++) {
                if (arr[j + 1] !== 0) {
                    arr[address] = arr[j + 1];
                    arr[j + 1] = 0;
                    j = length;
                }
            }
        }
    }
}

